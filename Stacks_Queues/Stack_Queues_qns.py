# 7. QUEUES
# A Queue is a FIFO (First In, First Out) data structure.

# Types of Queues:
# - Simple Queue: Basic FIFO operations
# - Circular Queue: Rear connects back to the front when the queue is full
# - Priority Queue: Elements are dequeued based on priority
# - Double-Ended Queue (Deque): Elements can be added/removed from both ends

# Basic Queue Operations:
# - Enqueue: Add an element to the rear of the queue
# - Dequeue: Remove an element from the front of the queue
# - Peek/Front: Get the front element without removing it
# - IsEmpty: Check if the queue is empty

